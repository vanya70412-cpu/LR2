n = int(input("Введіть число n (n ≤ 9999): "))

if n < 0:
    print("Негативне число, використаємо його абсолютне значення")
    n = abs(n)

if n > 9999:
    print("Число більше ніж 9999, використаємо останні 4 цифри")
    n = n % 10000

s4 = str(n).zfill(4)
print(f"4-значне представлення числа: {s4}")

if len(set(s4)) == 4:
    print("Усі цифри різні")
else:
    print("Є повторювані цифри")