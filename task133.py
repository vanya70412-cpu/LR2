a = float(input("Введіть поріг a: "))
b = float(input("Введіть поріг b: "))
c = float(input("Введіть поріг c: "))
s = float(input("Введіть суму s: "))

if not (a < b < c):
    print("Пороги не впорядковані, вони будуть впорядковані автоматично")
    thresholds = sorted([a, b, c])
    a, b, c = thresholds
    print(f"Упорядковані пороги: {a}, {b}, {c}")

if s <= 0:
    print("Сума непозитивна або нульова, податок дорівнює 0")
elif s <= a:
    print(f"Сума {s} менша або дорівнює порогу a={a}, податок = 0")
elif s <= b:
    print(f"Сума {s} між {a} і {b}, податок = 10% від суми")
    print(f"Податок = {0.10 * s}")
elif s <= c:
    print(f"Сума {s} між {b} і {c}, податок = 25% від суми")
    print(f"Податок = {0.25 * s}")
else:
    print(f"Сума {s} більша за {c}, податок = 50% від суми")
    print(f"Податок = {0.50 * s}")